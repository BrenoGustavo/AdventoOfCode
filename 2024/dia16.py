import heapq

def parse_maze(maze):
    start = None
    end = None
    grid = []
    for y, line in enumerate(maze.strip().split("\n")):
        row = []
        for x, char in enumerate(line):
            if char == 'S':
                start = (x, y)
                row.append('.')
            elif char == 'E':
                end = (x, y)
                row.append('.')
            else:
                row.append(char)
        grid.append(row)
    return grid, start, end

def valid_move(x, y, grid):
    return 0 <= y < len(grid) and 0 <= x < len(grid[0]) and grid[y][x] == '.'

def find_lowest_cost(maze):
    grid, start, end = parse_maze(maze)

    # Priority queue for A*
    pq = []
    heapq.heappush(pq, (0, start, 'E'))  # (cost, position, direction)

    # Directions: (dx, dy, direction)
    directions = {
        'N': (0, -1),
        'E': (1, 0),
        'S': (0, 1),
        'W': (-1, 0)
    }
    directions_list = ['N', 'E', 'S', 'W']

    # Visited set to track the minimum cost for each state
    visited = set()

    while pq:
        cost, (x, y), direction = heapq.heappop(pq)

        # Skip if already visited
        if ((x, y), direction) in visited:
            continue
        visited.add(((x, y), direction))

        # Check if we reached the end
        if (x, y) == end:
            return cost

        # Move forward
        dx, dy = directions[direction]
        nx, ny = x + dx, y + dy
        if valid_move(nx, ny, grid):
            heapq.heappush(pq, (cost + 1, (nx, ny), direction))

        # Rotate (clockwise and counterclockwise)
        for turn in [-1, 1]:
            new_dir_index = (directions_list.index(direction) + turn) % 4
            new_direction = directions_list[new_dir_index]
            heapq.heappush(pq, (cost + 1000, (x, y), new_direction))

    return -1  # No path found

